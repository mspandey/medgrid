def is_structured_query(query: str) -> bool:
    keywords = [
        "how many", "count", "maximum", "minimum",
        "most", "least", "available", "number of",
        "which hospital has", "total"
    ]
    query_lower = query.lower()
    return any(k in query_lower for k in keywords)

def pandas_answer(query, hospitals_df, doctors_df):
    q = query.lower()

    # ICU beds
    if "icu" in q and ("most" in q or "maximum" in q):
        result = hospitals_df.loc[
            hospitals_df['ICU_Beds'].idxmax()
        ]
        return f"""
🏥 Hospital with most ICU beds:
Name: {result['Name']}
ICU Beds: {int(result['ICU_Beds'])}
Address: {result['Address']}
Contact: {result['Contact']}
"""

    # Available doctors count
    if "how many" in q and "doctors" in q:
        count = doctors_df[doctors_df['Is_Available'] == True].shape[0]
        return f"👨‍⚕️ Total available doctors right now: {count}"

    # Blood availability
    for blood in ['A+', 'A-', 'B+', 'B-', 'AB+', 'AB-', 'O+', 'O-']:
        if blood.lower() in q:
            available = hospitals_df[hospitals_df[blood] > 0]
            if available.empty:
                return f"🩸 No hospitals currently have {blood} blood available."
            return "\n".join(
                f"🏥 {row['Name']} – {int(row[blood])} units"
                for _, row in available.iterrows()
            )

    return None

