def process_emergency_query(message, history):
    """Process user query and return response"""

    # Search for relevant hospitals and doctors
    hospital_results = search_hospitals(message)
    doctor_results = search_doctors(message)

    # Generate response using LLM
    response = llm.generate_response(message, hospital_results, doctor_results)

    # Also show formatted results for clarity
    formatted_results = format_results_for_display(hospital_results, doctor_results)

    # Combine responses
    full_response = f"{response}\n\n---\n{formatted_results}"

    return full_response

