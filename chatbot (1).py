class EmergencyDispatcherLLM:
    def __init__(self, model_id="microsoft/Phi-3-mini-4k-instruct", token=HF_TOKEN):
        self.client = InferenceClient(model=model_id, token=token)

    def generate_response(self, query, hospital_results, doctor_results):
        """Generate response using RAG results"""

        # Format hospital results
        hospital_text = "=== AVAILABLE HOSPITALS ===\n"
        if hospital_results and 'documents' in hospital_results and hospital_results['documents'][0]:
            for i, doc in enumerate(hospital_results['documents'][0]):
                metadata = hospital_results['metadatas'][0][i]
                hospital_text += f"\n🏥 {metadata['name']}\n"
                hospital_text += f"📍 {metadata['address']}\n"
                hospital_text += f"📞 {metadata['contact']}\n"
                # Extract key info from document
                doc_lines = doc.split('\n')
                for line in doc_lines:
                    if 'Beds:' in line or 'Blood:' in line or 'Specialties:' in line:
                        hospital_text += f"   {line.strip()}\n"
        else:
            hospital_text += "No matching hospitals found.\n"

        # Format doctor results
        doctor_text = "\n=== AVAILABLE DOCTORS ===\n"
        if doctor_results and 'documents' in doctor_results and doctor_results['documents'][0]:
            for i, doc in enumerate(doctor_results['documents'][0]):
                metadata = doctor_results['metadatas'][0][i]
                if metadata.get('available', False):  # Only show available doctors
                    doctor_text += f"\n👨‍⚕️ {metadata['name']}\n"
                    doctor_text += f"   Specialty: {metadata['specialty']}\n"
                    doctor_text += f"   Hospital: {metadata['hospital']}\n"
                    if metadata.get('experience'):
                        doctor_text += f"   Experience: {metadata['experience']} years\n"
        else:
            doctor_text += "No available doctors found.\n"

        # Create prompt for LLM
        prompt = f"""<|system|>
You are an Emergency Hospital Dispatcher AI. Your role is to help people find emergency medical services quickly and accurately.

Based on the search results below, provide a helpful response to the user's emergency query. Format your response clearly and include:
- Hospital names, addresses, and contact numbers
- Types of beds available
- Available doctors with their specialties
- Blood availability if relevant

Keep responses concise but include all critical information for an emergency.
and give only info about what they ask not other than this
<|end|>
<|user|>
Emergency Query: {query}

Search Results:
{hospital_text}
{doctor_text}
<|end|>
<|assistant|>"""

        try:
            response = self.client.text_generation(
                prompt,
                max_new_tokens=800,
                temperature=0.1,
                do_sample=True,
                return_full_text=False
            )
            return response
        except Exception as e:
            print(f"Error calling Hugging Face: {e}")
            return self._generate_fallback_response(hospital_text, doctor_text)

    def _generate_fallback_response(self, hospital_text, doctor_text):
        """Generate a simple response if LLM fails"""
        response = "Here are the available emergency resources I found:\n\n"
        response += hospital_text
        response += doctor_text
        return response

