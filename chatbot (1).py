def format_results_for_display(hospital_results, doctor_results):
    """Format search results for display"""
    output = []

    if hospital_results and hospital_results['documents'] and hospital_results['documents'][0]:
        output.append("🏥 **HOSPITALS WITH RESOURCES:**")
        for i, doc in enumerate(hospital_results['documents'][0][:3]):  # Show top 3
            metadata = hospital_results['metadatas'][0][i]
            output.append(f"\n**{metadata['name']}**")
            output.append(f"📍 {metadata['address']}")
            output.append(f"📞 {metadata['contact']}")

            # Extract bed info
            bed_lines = [line for line in doc.split('\n') if 'Beds:' in line]
            if bed_lines:
                output.append(f"🛏️ {bed_lines[0].strip()}")

            # Extract blood info
            blood_lines = [line for line in doc.split('\n') if 'Blood:' in line]
            if blood_lines:
                output.append(f"🩸 {blood_lines[0].strip()}")

            # Extract specialties
            spec_lines = [line for line in doc.split('\n') if 'Specialties:' in line]
            if spec_lines:
                output.append(f"🔬 {spec_lines[0].strip()}")

    if doctor_results and doctor_results['documents'] and doctor_results['documents'][0]:
        output.append("\n👨‍⚕️ **AVAILABLE DOCTORS:**")
        for i, doc in enumerate(doctor_results['documents'][0][:3]):  # Show top 3
            metadata = doctor_results['metadatas'][0][i]
            if metadata.get('available', False):
                output.append(f"\n**{metadata['name']}**")
                output.append(f"🔬 Specialty: {metadata['specialty']}")
                output.append(f"🏥 Hospital: {metadata['hospital']}")
                if metadata.get('experience'):
                    output.append(f"⭐ {metadata['experience']} years experience")

    return "\n".join(output)

